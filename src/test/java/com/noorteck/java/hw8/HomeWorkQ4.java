package com.noorteck.java.hw8;

public class HomeWorkQ4 {

	public static void main(String[] args) {

		int evenTotal = 0;
		int oddTotal = 0;
		int i = 1;
		do {
			
				i++;
		} while (i <= 20);

		
		if (i % 2 == 0) {
			evenTotal = evenTotal + i;

		} else {
			oddTotal = oddTotal + i;
		
		
		
		
	System.out.println(evenTotal);
	System.out.println(oddTotal);
		}
	
	}
}